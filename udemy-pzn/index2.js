//while loop
let testWhile = 1;
while (testWhile <= 10) {
  console.log(testWhile);
  testWhile++;
}

//do while loop
let testDoWhile = 1;
do {
  //blok perulangan
  console.log(testDoWhile);
  testDoWhile++;
} while (testDoWhile <= 10); //kondisi

//Break & Continue
let testBreak = 1;
while (true) {
  console.log(testBreak);
  testBreak++;
  if (testBreak > 10) break;
}

